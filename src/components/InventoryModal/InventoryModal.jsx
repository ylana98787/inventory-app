// КОМПОНЕНТ INVENTORY-MODAL (модальне вікно для перегляду товару)
// Призначення: показати детальну інформацію про вибраний товар
// Відкривається при натисканні на кнопку "👁️ Переглянути"

// Функція приймає 3 параметри (props):
// - isOpen : чи відкрите модальне вікно (true/false)
// - onClose: функція, яка закриває вікно
// - item   : об'єкт з даними товару (назва, опис, фото)
function InventoryModal({ isOpen, onClose, item }) {
  
  // ЯКЩО ВІКНО ЗАКРИТЕ (isOpen = false) АБО НЕМАЄ ТОВАРУ (item = null)
  // ТО НІЧОГО НЕ ПОКАЗУЄМО
  if (!isOpen || !item) return null;

  // ЯКЩО ВІКНО ВІДКРИТЕ І ТОВАР Є → ПОКАЗУЄМО
  return (
    // ПІДЛОЖКА (темний фон, що перекриває всю сторінку)
    // При кліку на фон → викликається onClose (закриває вікно)
    <div className="modal-overlay" onClick={onClose}>
      
      {/* ОСНОВНЕ ВІКНО (білий блок з вмістом) */}
      {/* e.stopPropagation() - забороняє кліку доходити до фону */}
      {/* Клік на білий блок НЕ закриває вікно */}
      <div className="inventory-modal" onClick={(e) => e.stopPropagation()}>
        
        {/* КНОПКА ЗАКРИТТЯ (хрестик у верхньому правому куті) */}
        <button className="modal-close" onClick={onClose}>×</button>
        
        {/* ВЕЛИКЕ ЗОБРАЖЕННЯ ТОВАРУ */}
        <img 
          src={item.photo_url}                    // джерело фото (з бази даних)
          alt={item.inventory_name}               // альтернативний текст (якщо фото не завантажилось)
          style={{ 
            width: '100%',                        // ширина на всю ширину вікна
            maxHeight: '300px',                   // максимальна висота 300px
            objectFit: 'cover'                   // фото заповнює всю площу (обрізається при потребі)
          }}
          // ЯКЩО ФОТО НЕ ЗАВАНТАЖИЛОСЬ (помилка)
          onError={(e) => {
            // Замінюємо на фото-заглушку (сіре поле з написом)
            e.target.src = 'https://via.placeholder.com/300';
          }}
        />
        
        {/* НАЗВА ТОВАРУ (великий заголовок) */}
        <h2>{item.inventory_name}</h2>
        
        {/* ОПИС ТОВАРУ */}
        {/* Якщо опису немає (null, undefined або пустий рядок) → показуємо "Немає опису" */}
        <p>{item.description || 'Немає опису'}</p>
        
      </div>
    </div>
  );
}

// ЕКСПОРТУЄМО КОМПОНЕНТ ДЛЯ ВИКОРИСТАННЯ В ІНШИХ ФАЙЛАХ
export default InventoryModal;