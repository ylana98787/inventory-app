// ІМПОРТУЄМО КОМПОНЕНТ Link З React Router ДЛЯ НАВІГАЦІЇ
// Link - це посилання, яке працює без перезавантаження сторінки
import { Link } from 'react-router-dom';

// КОМПОНЕНТ LAYOUT (ШАПКА + ОСНОВНА ОБЛАСТЬ)
// Це "обгортка" для всіх сторінок. Він відповідає за загальний вигляд:
// - Шапка з навігацією (завжди видна)
// - Основний контент (children - те, що буде всередині)
// Приймає один параметр (props):
// - children : вміст, який буде відображатися всередині <main>
function Layout({ children }) {
  return (
    // ГОЛОВНИЙ КОНТЕЙНЕР ЛЕЙАУТУ
    <div className="layout">
      
      {/* ===== ШАПКА (HEADER) ===== */}
      <header className="header">
        
        {/* ЗАГОЛОВОК САЙТУ */}
        <h1>🏭 Адмін-панель складу</h1>
        
        {/* НАВІГАЦІЙНЕ МЕНЮ */}
        <nav>
          {/* ПОСИЛАННЯ НА СТОРІНКУ /admin (Інвентар) */}
          {/* to="/admin" - куди перейти при кліку */}
          <Link to="/admin">📦 Інвентар</Link>
          
          {/* ПОСИЛАННЯ НА СТОРІНКУ / (Головна / Галерея) */}
          <Link to="/">🎨 Галерея</Link>
        </nav>
      </header>
      
      {/* ===== ОСНОВНИЙ КОНТЕНТ ===== */}
      {/* Тут буде підставлятися вміст сторінки (AdminPage або GalleryPage) */}
      <main className="main">{children}</main>
      
    </div>
  );
}

// ЕКСПОРТУЄМО КОМПОНЕНТ ДЛЯ ВИКОРИСТАННЯ В ІНШИХ ФАЙЛАХ
export default Layout;