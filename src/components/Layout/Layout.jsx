// ІМПОРТ КОМПОНЕНТА Link З БІБЛІОТЕКИ REACT ROUTER
// Link - це посилання, яке працює БЕЗ ПЕРЕЗАВАНТАЖЕННЯ сторінки
// На відміну від звичайного <a href="...">, Link не перезавантажує всю сторінку,
// а змінює тільки той контент, який потрібно
import { Link } from 'react-router-dom';

// КОМПОНЕНТ LAYOUT - "ОБГОРТКА" ДЛЯ ВСІХ СТОРІНОК
// Він відповідає за те, що буде показано на кожній сторінці:
// - Шапка (header) з навігацією
// - Основний контент (children)
// Це дозволяє не дублювати код шапки на кожній сторінці
function Layout({ children }) {
  return (
    // ГОЛОВНИЙ КОНТЕЙНЕР ЛЕЙАУТУ
    <div className="layout">
      
      {/* ШАПКА САЙТУ (HEADER) - ОДНАКОВА ДЛЯ ВСІХ СТОРІНОК */}
      <header className="header">
        <h1>🏭 Система управління складом</h1>
        
        {/* НАВІГАЦІЙНЕ МЕНЮ */}
        <nav>
          {/* ПОСИЛАННЯ НА ГОЛОВНУ СТОРІНКУ (ГАЛЕРЕЮ) */}
          <Link to="/">🎨 Галерея</Link>
          
          {/* ПОСИЛАННЯ НА СТОРІНКУ УЛЮБЛЕНИХ */}
          <Link to="/favorites">❤️ Улюблені</Link>
          
          {/* ПОСИЛАННЯ НА АДМІН-ПАНЕЛЬ */}
          <Link to="/admin">⚙️ Адмін-панель</Link>
        </nav>
      </header>
      
      {/* ОСНОВНИЙ КОНТЕНТ - ТУТ ПІДСТАВЛЯЄТЬСЯ ПОТОЧНА СТОРІНКА */}
      {/* {children} - це те, що знаходиться всередині <Layout> ... </Layout> в App.jsx */}
      <main className="main">{children}</main>
      
    </div>
  );
}

// ЕКСПОРТУЄМО КОМПОНЕНТ ДЛЯ ВИКОРИСТАННЯ В ІНШИХ ФАЙЛАХ
export default Layout;